#ifndef VGA_PALETTE_H
#define VGA_PALETTE_H

// Standard VGA 16 color palette
#define VGA16_BLACK				0		// 0x00, 0x00, 0x00
#define VGA16_BLUE				1		// 0x00, 0x00, 0xAA
#define VGA16_GREEN				2		// 0x00, 0xAA, 0x00
#define VGA16_CYAN				3		// 0x00, 0xAA, 0xAA
#define VGA16_RED				4		// 0xAA, 0x00, 0x00
#define VGA16_MAGENTA			5		// 0xAA, 0x00, 0xAA
#define VGA16_BROWN				6		// 0xAA, 0x55, 0x00
#define VGA16_GRAY				7		// 0xAA, 0xAA, 0xAA
#define VGA16_DARK_GRAY			8		// 0x55, 0x55, 0x55
#define VGA16_BRIGHT_BLUE		9		// 0x55, 0x55, 0xFF
#define VGA16_BRIGHT_GREEN		10		// 0x55, 0xFF, 0x55
#define VGA16_BRIGHT_CYAN		11		// 0x55, 0xFF, 0xFF
#define VGA16_BRIGHT_RED		12		// 0xFF, 0x55, 0x55
#define VGA16_BRIGHT_MAGENTA	13		// 0xFF, 0x55, 0xFF
#define VGA16_YELLOW			14		// 0xFF, 0xFF, 0x55
#define VGA16_WHITE				15		// 0xFF, 0xFF, 0xFF

#endif // VGA_PALETTE_H
